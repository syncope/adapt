# this is the first basis for parameter estimation
# it provides some helper functionality to scipy.optimize module

import scipy.optimize
import numpy as np



def fit(function, parameters, y, x=None):
    def f(params):
        i = 0
        for p in parameters:
            p.set(params[i])
            i += 1
        return y - function(x)

    if x is None:
        x = np.arange(y.shape[0])

    scipy.optimize.leastsq(f, p)
